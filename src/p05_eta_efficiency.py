# ================================================================
# LINEAR INTERPOLATION
# ================================================================

def linear_interpolate(x, x1, y1, x2, y2):

    return y1 + (
        (y2 - y1)
        * (x - x1)
        / (x2 - x1)
    )


# ================================================================
# SELECTION FUNCTION
# ================================================================

def select_efficiency(power_kw, poles, protection):

    protection = protection.upper()

    if protection not in ETA_DATA:

        raise ValueError(
            f"Unsupported protection degree: {protection}"
        )

    if poles not in ETA_DATA[protection]:

        raise ValueError(
            f"No eta data for 2p = {poles}"
        )

    points = ETA_DATA[protection][poles]

    P_min = points[0][0]
    P_max = points[-1][0]

    if power_kw < P_min or power_kw > P_max:

        raise ValueError(
            f"P2 = {power_kw} kW is outside eta "
            f"diagram range {P_min} ... {P_max} kW."
        )

    for P_point, eta_point in points:

        if abs(power_kw - P_point) < 1e-9:

            return round(
                eta_point,
                3,
            )

    for index in range(len(points) - 1):

        P1, eta1 = points[index]
        P2, eta2 = points[index + 1]

        if P1 <= power_kw <= P2:

            eta = linear_interpolate(
                power_kw,
                P1,
                eta1,
                P2,
                eta2,
            )

            return round(
                eta,
                3,
            )

    raise ValueError(
        "Unable to determine efficiency eta."
    )


