# ================================================================
# LINEAR INTERPOLATION
# ================================================================

def linear_interpolate(x, x1, y1, x2, y2):

    return y1 + (
        (y2 - y1)
        * (x - x1)
        / (x2 - x1)
    )


# ================================================================
# SELECTION FUNCTION
# ================================================================

def select_power_factor(power_kw, poles, protection):

    protection = protection.upper()

    if protection not in COS_PHI_DATA:

        raise ValueError(
            f"Unsupported protection degree: {protection}"
        )

    if poles not in COS_PHI_DATA[protection]:

        raise ValueError(
            f"No cos(phi) data for 2p = {poles}"
        )

    points = COS_PHI_DATA[protection][poles]

    P_min = points[0][0]
    P_max = points[-1][0]

    if power_kw < P_min or power_kw > P_max:

        raise ValueError(
            f"P2 = {power_kw} kW is outside cos(phi) "
            f"diagram range {P_min} ... {P_max} kW."
        )

    for P_point, cos_phi_point in points:

        if abs(power_kw - P_point) < 1e-9:

            return round(
                cos_phi_point,
                3,
            )

    for index in range(len(points) - 1):

        P1, cos_phi1 = points[index]
        P2, cos_phi2 = points[index + 1]

        if P1 <= power_kw <= P2:

            cos_phi = linear_interpolate(
                power_kw,
                P1,
                cos_phi1,
                P2,
                cos_phi2,
            )

            return round(
                cos_phi,
                3,
            )

    raise ValueError(
        "Unable to determine cos(phi)."
    )


