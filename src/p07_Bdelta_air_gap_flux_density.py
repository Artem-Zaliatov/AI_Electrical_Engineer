# ================================================================
# AUXILIARY FUNCTIONS
# ================================================================

def select_ip44_size_group(h):

    if h <= 132:
        return "SMALL"

    if h <= 250:
        return "MEDIUM"

    return "LARGE"


def select_ip23_diagram(Da):

    if 0.26 <= Da <= 0.48:
        return "A"

    if 0.50 <= Da <= 0.70:
        return "B"

    raise ValueError(
        f"Da = {Da:.3f} m is outside Figure 9.23 range."
    )


def linear_interpolate(x, x1, y1, x2, y2):

    return y1 + (
        (y2 - y1)
        * (x - x1)
        / (x2 - x1)
    )


def interpolate_range(Da, points):

    Da_min = points[0][0]
    Da_max = points[-1][0]

    if Da < Da_min or Da > Da_max:
        raise ValueError(
            f"Da = {Da:.3f} m is outside diagram "
            f"range {Da_min:.3f} ... {Da_max:.3f} m."
        )

    for Da_point, value_min, value_max in points:

        if abs(Da - Da_point) < 1e-9:

            return value_min, value_max

    for index in range(len(points) - 1):

        Da1, value_min1, value_max1 = points[index]
        Da2, value_min2, value_max2 = points[index + 1]

        if Da1 <= Da <= Da2:

            value_min = linear_interpolate(
                Da,
                Da1,
                value_min1,
                Da2,
                value_min2,
            )

            value_max = linear_interpolate(
                Da,
                Da1,
                value_max1,
                Da2,
                value_max2,
            )

            return value_min, value_max

    raise ValueError(
        "Unable to interpolate B_delta range."
    )


# ================================================================
# SELECTION FUNCTION
# ================================================================

def select_air_gap_flux_density(
    h,
    Da,
    poles,
    protection,
):

    protection = protection.upper()

    if protection == "IP44":

        group = select_ip44_size_group(h)

        data = B_DELTA_DATA_IP44[group]

    elif protection == "IP23":

        diagram = select_ip23_diagram(Da)

        data = B_DELTA_DATA_IP23[diagram]

    else:

        raise ValueError(
            f"Unsupported protection degree: {protection}"
        )

    if poles not in data:

        raise ValueError(
            f"No B_delta data for 2p = {poles}, "
            f"protection = {protection}."
        )

    points = data[poles]

    B_min, B_max = interpolate_range(
        Da,
        points,
    )

    B_delta = (
        B_min + B_max
    ) / 2

    return (
        round(B_delta, 3),
        round(B_min, 3),
        round(B_max, 3),
    )


